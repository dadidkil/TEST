# -*- coding: utf-8 -*-
"""
–ú–æ–¥—É–ª—å –¥–ª—è –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–æ–≥–æ –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∏ —Å–µ—Ä–≤–µ—Ä–∞ –≤ –Ω–∞–∑–≤–∞–Ω–∏—è—Ö –∫–∞–Ω–∞–ª–æ–≤.
"""
import discord
from discord.ext import commands, tasks
import asyncio

# --- –ö–û–ù–§–ò–ì–£–†–ê–¶–ò–Ø ---
# –ö–∞–∫ —á–∞—Å—Ç–æ (–≤ –º–∏–Ω—É—Ç–∞—Ö) –æ–±–Ω–æ–≤–ª—è—Ç—å —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É. 
# –ù–µ —Å—Ç–∞–≤—å—Ç–µ —Å–ª–∏—à–∫–æ–º –Ω–∏–∑–∫–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ, —á—Ç–æ–±—ã –Ω–µ –ø–æ–ª—É—á–∏—Ç—å –±–∞–Ω –æ—Ç Discord. 10 - –æ–ø—Ç–∏–º–∞–ª—å–Ω–æ.
UPDATE_INTERVAL_MINUTES = 10

# –ü—Ä–µ—Ñ–∏–∫—Å—ã –∫–∞–Ω–∞–ª–æ–≤, –∫–æ—Ç–æ—Ä—ã–µ –±–æ—Ç –±—É–¥–µ—Ç –∏—Å–∫–∞—Ç—å –¥–ª—è –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è.
# –í–∞–∂–Ω–æ, —á—Ç–æ–±—ã –æ–Ω–∏ –≤ —Ç–æ—á–Ω–æ—Å—Ç–∏ —Å–æ–≤–ø–∞–¥–∞–ª–∏ —Å –Ω–∞–∑–≤–∞–Ω–∏—è–º–∏ –≤ —Ñ–∞–π–ª–µ —Å—Ç—Ä—É–∫—Ç—É—Ä—ã.
MEMBER_CHANNEL_PREFIX = "‚ïî üìö„Éª–£—á–∞—Å—Ç–Ω–∏–∫–∏:"
BOT_CHANNEL_PREFIX = "‚ïö üìö„Éª–ë–æ—Ç—ã:"


class StatsUpdater(commands.Cog):
    """
    –ö–æ–≥, –æ—Ç–≤–µ—á–∞—é—â–∏–π –∑–∞ –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–æ–µ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∏
    —Å–µ—Ä–≤–µ—Ä–∞ (–∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —É—á–∞—Å—Ç–Ω–∏–∫–æ–≤ –∏ –±–æ—Ç–æ–≤) –≤ –Ω–∞–∑–≤–∞–Ω–∏—è—Ö –≥–æ–ª–æ—Å–æ–≤—ã—Ö –∫–∞–Ω–∞–ª–æ–≤.
    """

    def __init__(self, bot: commands.Bot):
        self.bot = bot
        self.update_stats_task.start()

    def cog_unload(self):
        """–í—ã–∑—ã–≤–∞–µ—Ç—Å—è –ø—Ä–∏ –≤—ã–≥—Ä—É–∑–∫–µ –∫–æ–≥–∞, –æ—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ—Ç –∑–∞–¥–∞—á—É."""
        self.update_stats_task.cancel()

    @tasks.loop(minutes=UPDATE_INTERVAL_MINUTES)
    async def update_stats_task(self):
        """
        –ì–ª–∞–≤–Ω–∞—è –∑–∞–¥–∞—á–∞, –∫–æ—Ç–æ—Ä–∞—è –ø–µ—Ä–∏–æ–¥–∏—á–µ—Å–∫–∏ –∑–∞–ø—É—Å–∫–∞–µ—Ç –æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ
        —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∏ –¥–ª—è –≤—Å–µ—Ö —Å–µ—Ä–≤–µ—Ä–æ–≤, –≥–¥–µ –Ω–∞—Ö–æ–¥–∏—Ç—Å—è –±–æ—Ç.
        """
        print("–ó–∞–ø—É—Å–∫–∞—é —Ñ–æ–Ω–æ–≤—É—é –∑–∞–¥–∞—á—É –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∏ –∫–∞–Ω–∞–ª–æ–≤...")
        for guild in self.bot.guilds:
            try:
                await self._update_single_guild(guild)
            except Exception as e:
                print(f"–ö—Ä–∏—Ç–∏—á–µ—Å–∫–∞—è –æ—à–∏–±–∫–∞ –ø—Ä–∏ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–∏ —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∏ –¥–ª—è —Å–µ—Ä–≤–µ—Ä–∞ {guild.name} ({guild.id}): {e}")
        print("–ó–∞–¥–∞—á–∞ –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∏ –∑–∞–≤–µ—Ä—à–µ–Ω–∞.")
    
    @update_stats_task.before_loop
    async def before_update_stats(self):
        """–û–∂–∏–¥–∞–µ—Ç –ø–æ–ª–Ω–æ–π –≥–æ—Ç–æ–≤–Ω–æ—Å—Ç–∏ –±–æ—Ç–∞ –ø–µ—Ä–µ–¥ –ø–µ—Ä–≤—ã–º –∑–∞–ø—É—Å–∫–æ–º –∑–∞–¥–∞—á–∏."""
        await self.bot.wait_until_ready()
        print("–ú–æ–¥—É–ª—å —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∏ –≥–æ—Ç–æ–≤ –∫ —Ä–∞–±–æ—Ç–µ. –ó–∞–ø—É—Å–∫–∞—é —Ü–∏–∫–ª –æ–±–Ω–æ–≤–ª–µ–Ω–∏–π.")

    async def _update_single_guild(self, guild: discord.Guild):
        """–û–±–Ω–æ–≤–ª—è–µ—Ç —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É –¥–ª—è –æ–¥–Ω–æ–≥–æ –∫–æ–Ω–∫—Ä–µ—Ç–Ω–æ–≥–æ —Å–µ—Ä–≤–µ—Ä–∞."""
        
        # –ò—â–µ–º –∫–∞–Ω–∞–ª—ã –ø–æ –ø—Ä–µ—Ñ–∏–∫—Å–∞–º. –ò—Å–ø–æ–ª—å–∑—É–µ–º discord.utils.find –¥–ª—è —ç—Ñ—Ñ–µ–∫—Ç–∏–≤–Ω–æ—Å—Ç–∏.
        member_channel = discord.utils.find(lambda c: c.name.startswith(MEMBER_CHANNEL_PREFIX), guild.voice_channels)
        bot_channel = discord.utils.find(lambda c: c.name.startswith(BOT_CHANNEL_PREFIX), guild.voice_channels)
        
        # –ï—Å–ª–∏ –Ω–∞ —Å–µ—Ä–≤–µ—Ä–µ –Ω–µ—Ç –Ω—É–∂–Ω—ã—Ö –∫–∞–Ω–∞–ª–æ–≤, –Ω–∏—á–µ–≥–æ –Ω–µ –¥–µ–ª–∞–µ–º.
        if not member_channel and not bot_channel:
            return

        # --- –í—ã—á–∏—Å–ª—è–µ–º —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É ---
        # guild.member_count - —Å–∞–º—ã–π –Ω–∞–¥–µ–∂–Ω—ã–π —Å–ø–æ—Å–æ–± –ø–æ–ª—É—á–∏—Ç—å –æ–±—â–µ–µ —á–∏—Å–ª–æ —É—á–∞—Å—Ç–Ω–∏–∫–æ–≤.
        total_members = guild.member_count
        # –î–ª—è –ø–æ–¥—Å—á–µ—Ç–∞ –±–æ—Ç–æ–≤ –Ω–µ–æ–±—Ö–æ–¥–∏–º –∫–µ—à —É—á–∞—Å—Ç–Ω–∏–∫–æ–≤, –∫–æ—Ç–æ—Ä—ã–π –¥–æ–ª–∂–µ–Ω –±—ã—Ç—å –≤–∫–ª—é—á–µ–Ω.
        bot_count = sum(1 for member in guild.members if member.bot)
        
        # --- –û–±–Ω–æ–≤–ª—è–µ–º –∫–∞–Ω–∞–ª —Å —É—á–∞—Å—Ç–Ω–∏–∫–∞–º–∏ ---
        if member_channel:
            new_name = f"{MEMBER_CHANNEL_PREFIX} {total_members}"
            # –û–±–Ω–æ–≤–ª—è–µ–º –∏–º—è, —Ç–æ–ª—å–∫–æ –µ—Å–ª–∏ –æ–Ω–æ –∏–∑–º–µ–Ω–∏–ª–æ—Å—å, —á—Ç–æ–±—ã –∏–∑–±–µ–∂–∞—Ç—å –ª–∏—à–Ω–∏—Ö –∑–∞–ø—Ä–æ—Å–æ–≤ –∫ API.
            if member_channel.name != new_name:
                try:
                    await member_channel.edit(name=new_name, reason="–û–±–Ω–æ–≤–ª–µ–Ω–∏–µ —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∏ —É—á–∞—Å—Ç–Ω–∏–∫–æ–≤")
                    print(f"–°–µ—Ä–≤–µ—Ä '{guild.name}': –ö–∞–Ω–∞–ª —É—á–∞—Å—Ç–Ω–∏–∫–æ–≤ –æ–±–Ω–æ–≤–ª–µ–Ω ({total_members}).")
                    # –ù–µ–±–æ–ª—å—à–∞—è –ø–∞—É–∑–∞, —á—Ç–æ–±—ã –Ω–µ –æ—Ç–ø—Ä–∞–≤–ª—è—Ç—å –∑–∞–ø—Ä–æ—Å—ã —Å–ª–∏—à–∫–æ–º —á–∞—Å—Ç–æ.
                    await asyncio.sleep(2)
                except discord.Forbidden:
                    print(f"–û—à–∏–±–∫–∞ –Ω–∞ —Å–µ—Ä–≤–µ—Ä–µ '{guild.name}': –Ω–µ—Ç –ø—Ä–∞–≤ –¥–ª—è —Ä–µ–¥–∞–∫—Ç–∏—Ä–æ–≤–∞–Ω–∏—è –∫–∞–Ω–∞–ª–∞ —É—á–∞—Å—Ç–Ω–∏–∫–æ–≤.")
                except Exception as e:
                    print(f"–ù–µ–ø—Ä–µ–¥–≤–∏–¥–µ–Ω–Ω–∞—è –æ—à–∏–±–∫–∞ –ø—Ä–∏ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–∏ –∫–∞–Ω–∞–ª–∞ —É—á–∞—Å—Ç–Ω–∏–∫–æ–≤ –Ω–∞ '{guild.name}': {e}")
        
        # --- –û–±–Ω–æ–≤–ª—è–µ–º –∫–∞–Ω–∞–ª —Å –±–æ—Ç–∞–º–∏ ---
        if bot_channel:
            new_name = f"{BOT_CHANNEL_PREFIX} {bot_count}"
            if bot_channel.name != new_name:
                try:
                    await bot_channel.edit(name=new_name, reason="–û–±–Ω–æ–≤–ª–µ–Ω–∏–µ —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∏ –±–æ—Ç–æ–≤")
                    print(f"–°–µ—Ä–≤–µ—Ä '{guild.name}': –ö–∞–Ω–∞–ª –±–æ—Ç–æ–≤ –æ–±–Ω–æ–≤–ª–µ–Ω ({bot_count}).")
                except discord.Forbidden:
                    print(f"–û—à–∏–±–∫–∞ –Ω–∞ —Å–µ—Ä–≤–µ—Ä–µ '{guild.name}': –Ω–µ—Ç –ø—Ä–∞–≤ –¥–ª—è —Ä–µ–¥–∞–∫—Ç–∏—Ä–æ–≤–∞–Ω–∏—è –∫–∞–Ω–∞–ª–∞ –±–æ—Ç–æ–≤.")
                except Exception as e:
                    print(f"–ù–µ–ø—Ä–µ–¥–≤–∏–¥–µ–Ω–Ω–∞—è –æ—à–∏–±–∫–∞ –ø—Ä–∏ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–∏ –∫–∞–Ω–∞–ª–∞ –±–æ—Ç–æ–≤ –Ω–∞ '{guild.name}': {e}")


def setup(bot: commands.Bot):
    """–§—É–Ω–∫—Ü–∏—è, –∫–æ—Ç–æ—Ä—É—é discord.py –≤—ã–∑—ã–≤–∞–µ—Ç –¥–ª—è –∑–∞–≥—Ä—É–∑–∫–∏ –∫–æ–≥–∞."""
    bot.add_cog(StatsUpdater(bot)) 